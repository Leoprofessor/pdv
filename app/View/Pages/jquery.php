    <article class="markdown-body entry-content" itemprop="mainContentOfPage"><h1>
<a id="user-content-formbuilder-v2" class="anchor" href="#formbuilder-v2" aria-hidden="true"><span class="octicon octicon-link"></span></a>FormBuilder v2</h1>

<p>Formbuilder is a component for your web-application that allows users to build web forms. We provide
basic logic for rendering the resulting form. </p>

<p>Think of it like a CMS for forms.</p>

<h3>
<a id="user-content-requirements" class="anchor" href="#requirements" aria-hidden="true"><span class="octicon octicon-link"></span></a>Requirements</h3>

<p>Formbuilder 2 requires a few tools, but we include them in the <code>dist</code> package for your convenience.</p>

<ul class="task-list">
<li>
<code>lodash</code> - An excellent (fast/light-weight) javascript utility library</li>
<li>
<code>jQuery</code> - A DOM manipulation/ajax library</li>
<li>
<code>dustjs</code> - A javascript templating engine</li>
</ul>

<h3>
<a id="user-content-whats-different-in-v2" class="anchor" href="#whats-different-in-v2" aria-hidden="true"><span class="octicon octicon-link"></span></a>What's Different In v2</h3>

<p>The web has changed so much in the five years since I wrote v1, so there's a lot of newer technology
we can use - JSON over xml, lodash, templating, MongoDB, etc.</p>

<p>v1 was pretty bulky and poorly coded. It was designed for a specific purpose and wasn't flexible enough
for how people have used formbuilder ever since. <a href="https://github.com/viveleroi/jquery.formbuilder/tree/1.0">If you need v1, see here.</a></p>

<p>Version 2 offers:</p>

<ul class="task-list">
<li>JSON all the way around. No serliazing/format conversion.</li>
<li>More flexible field schema options for future expansion work</li>
<li>Form editor and form rendering use templates, making it easier to customize them</li>
<li>Better re-use of internal code, much cleaner code</li>
<li>Returns a basic API you can use for custom work</li>
<li>PHP/Mongo save/load example code is extremely lightweight, and easily replaced</li>
<li>Much improved support for multiple instances on a single page</li>
</ul>

<p>v2 uses an internal javascript object that we keep synchronized with the form editor. When you load/save
we're simply passing the object (JSON) around. </p>

<p>While you can store this data in any database, I've opted for MongoDB as an example because it stores
data as JSON documents. So your data is JSON all the way around. Plus, I absolutely love it.</p>

<p>However, php/mongo aren't required and can be easily replaced with any back-end storage/language.</p>

<h3>
<a id="user-content-important-notes" class="anchor" href="#important-notes" aria-hidden="true"><span class="octicon octicon-link"></span></a>Important Notes</h3>

<p>Unlike v1, we're now leaving validation and processing of actual form submissions up to you. While validation may
be improved, processing and handling form data is something we can't predict for your use case.</p>

<h3>
<a id="user-content-distribusion-code" class="anchor" href="#distribusion-code" aria-hidden="true"><span class="octicon octicon-link"></span></a>Distribusion Code</h3>

<p>We provide a <code>dist</code> folder with a complete copy of everything you need to get started. Our build process copies over
and compiles the libraries and source for production-ready environments.</p>

<h3>
<a id="user-content-issuesfeedback" class="anchor" href="#issuesfeedback" aria-hidden="true"><span class="octicon octicon-link"></span></a>Issues/Feedback</h3>

<p>Please report all issues, bugs, and feature suggestions to our <a href="https://snowy-evening.com/botsko/jquery-form-builder/">issue tracker</a></p>

<p>Frombuilder is open source, we encourage you to submit improvements!</p>

<h3>
<a id="user-content-development" class="anchor" href="#development" aria-hidden="true"><span class="octicon octicon-link"></span></a>Development</h3>

<p>After cloning the repo, be sure you've installed the proper node/bower modules:</p>

<ul class="task-list">
<li><code>npm install</code></li>
<li><code>bower install</code></li>
</ul>

<p>Then, run <code>grunt</code> to properly check and package the files.</p>

